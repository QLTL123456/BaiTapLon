CREATE TABLE PHONGBAN
(
	MAPB VARCHAR(10) NOT NULL PRIMARY KEY,
	TENPB NVARCHAR(50) NOT NULL
);

CREATE TABLE CHUCVU
(
	MACV VARCHAR(10)  NOT NULL PRIMARY KEY,
	TENCV NVARCHAR(50) NOT NULL
);

CREATE TABLE NHANVIEN
(
	MANV VARCHAR(10) NOT NULL PRIMARY KEY,
	HOTENNV NVARCHAR(50) NOT NULL,
	DIACHI NVARCHAR(100) NOT NULL,
	THUONGTRU NVARCHAR(100) NOT NULL,
	SODIENTHOAI INT NOT NULL,
	NGAYSINH DATE NOT NULL,
	NOISINH NVARCHAR(100) NOT NULL,
	GIOITINH BIT NOT NULL,
	CMND INT NOT NULL,
	QUEQUAN NVARCHAR(100) NOT NULL,
	DANTOC NVARCHAR(15) NOT NULL,
	GHICHU NVARCHAR(500) NULL,
	MAPB VARCHAR(10) NOT NULL foreign key (MAPB) references PHONGBAN(MAPB),
	MACV VARCHAR(10) NOT NULL foreign key (MACV) references CHUCVU(MACV),
	MATKHAU NVARCHAR(32) NOT NULL
);

CREATE TABLE QUATRINHCONGTAC
(
	MAQTCT VARCHAR(10) NOT NULL PRIMARY KEY,
	MANV VARCHAR(10) NOT NULL foreign key (MANV) references NHANVIEN(MANV),
	TGBATDAU DATE,
	TGKETTHUC DATE,
	TENCONGTY NVARCHAR(50) NOT NULL
);

CREATE TABLE BANGCAP
(
	MABC VARCHAR(10) NOT NULL PRIMARY KEY,
	MANV VARCHAR(10) NOT NULL foreign key (MANV) references NHANVIEN(MANV),
	TENBC NVARCHAR(50) NOT NULL,
	LOAIBC NVARCHAR(30) NOT NULL,
	NGAYCAP DATE,
	DONVICAP NVARCHAR(70) NOT NULL
);

CREATE TABLE LUONG
(
	MALUONG VARCHAR(10) NOT NULL PRIMARY KEY,
	MANV VARCHAR(10) NOT NULL foreign key (MANV) references NHANVIEN(MANV),
	THANG INT NOT NULL,
	LUONGTAMUNG FLOAT,
	NGAYLAMVIEC INT,
	BHXH float,
	BHYT float,
	TIENLUONG float
);

CREATE TABLE THUONG
(
	MANV VARCHAR(10) NOT NULL foreign key (MANV) references NHANVIEN(MANV),
	DIPTHUONG VARCHAR(50) NOT NULL,
	TIENTHUONG FLOAT NOT NULL,
	CONSTRAINT PK PRIMARY KEY(MANV)
);

create table QUANTRI
(
	ID VARCHAR(32) NOT NULL primary key,
	MATKHAU NVARCHAR(32) NOT NULL,
	HOTEN NVARCHAR(50) NOT NULL
);

